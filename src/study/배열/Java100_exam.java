package study.배열;

public class Java100_exam {
    public static void main(String[] args) {

        //65는 ASCII 코드 A는 65 Z 는 90
        //90 Z <<
        for (int i = 65; i <= 90; i++) {

            //char을붙여서 문자로변환                    A ~Z
            System.out.print((char) i + " "); // 65 66 67 68 69 70..........90
        }

        System.out.println();
    }
}
